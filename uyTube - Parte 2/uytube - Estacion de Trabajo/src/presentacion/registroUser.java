    private void jButtonSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSiguienteActionPerformed
        //Mover a registrarCanal
        try {
            // Me aseguro de que la imagen es correcta y la guardo
            File f = fc.getSelectedFile();
            String imagen = "";
            String fpath = "";
            String fnewpath = "";
            if(f != null){
                fpath = f.getPath();
                String extension = "";
                int i = fpath.lastIndexOf('.');
                if (i > 0) extension = fpath.substring(i+1);

                if (!extension.equals("jpg") && !extension.equals("png"))
                    throw new Exception(extension+" Imagen con extension invalida, por favor suba una imagen .jpg o .png");

                imagen = textField3.getText() + "." + extension;
                fnewpath = "data/imagenes/" + imagen;
            }
            //AltaUsuario(String nick,String passw, String nom, String apell, String mail, Date fnac, String img)
            //Creo el usuario
            int existe = u.obtenerIdUsuario(textField3.getText());
            u.AltaUsuario(
                textField3.getText(),
                textField5.getText(),    
                textField1.getText(),
                textField2.getText(), 
                textField4.getText(),
                jDateChooser1.getDate(),
                imagen
            );
            
            //Copio el archivo al directorio de imagenes, lo hago luego por si el nick ya existe
            if(!fpath.equals(fnewpath) && !fnewpath.isEmpty()) FileUtils.copyFile(f, new File(fnewpath));
            
            if(existe != u.obtenerIdUsuario(textField3.getText())) {
                p.AgregarInternalFrame(new registroCanal(textField3.getText()));
                x=null;
                u=null;
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error: "+e.getMessage());
        }
    }//GEN-LAST:event_jButtonSiguienteActionPerformed

